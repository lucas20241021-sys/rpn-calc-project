RPN 计算器使用说明书"

1. 安装和编译指南"
在终端中编译并运行本程序：
```bash
g++ -std=c++17 -O2 rpn_calc.cpp -o rpn_calc
./rpn_calc

2. 基本使用示例"
> 3 4 +
结果: 7
> 5 1 2 + 4 * + 3 -
结果: 14
"""
3. 所有支持的操作说明"
数字输入：支持整数和浮点数
四则运算：
+ 加法

- 减法

* 乘法

/ 除法

高级功能：
^ 幂运算
sqrt 平方根

栈操作：
stack 显示当前栈
clear 清空栈

系统命令：

q / quit / exit 退出程序
"""

4. 错误代码和异常说明"

操作数不足：执行运算时栈内元素数量不足

除零错误：执行除法时除数为 0

平方根不能是负数：对负数执行 sqrt

未知符号：输入了无法识别的字符串

表达式无结果：整行表达式执行后栈为空
5. 示例输入输出"
> 2 3 ^
结果: 8
> 9 sqrt
结果: 3
> 7 0 /
错误: 除零错误
> 5 5 + 3 +
结果: 13
> stack
[栈顶] 13 [栈底]
> clear
> stack
[栈顶] [栈底]
